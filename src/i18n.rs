/// Dynamic translation helper for strings that need formatting.
/// Use `t()` for static strings; use this for strings with placeholders.
pub fn tf(key: &'static str, lang: Language, args: &[&str]) -> String {
    use Language::*;
    match (key, lang) {
        ("panic.remaining", Fr) => format!("Mot de passe incorrect. {} tentative(s) restante(s).", args.first().unwrap_or(&"?")),
        ("panic.remaining", En) => format!("Incorrect password. {} attempt(s) remaining.", args.first().unwrap_or(&"?")),
        ("recovery.error_fmt", Fr) => format!("Erreur : {}", args.first().unwrap_or(&"")),
        ("recovery.error_fmt", En) => format!("Error: {}", args.first().unwrap_or(&"")),
        _ => t(key, lang).to_string(),
    }
}
